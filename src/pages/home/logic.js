const sarlem = (n) => {
  if (n === 5) {
    return "A";
  }
  if (n === 4) {
    return "B";
  }
  if (n === 3) {
    return "C";
  }
  if (n === 2) {
    return "D";
  }
  if (n === 0) {
    return "F";
  }
};

