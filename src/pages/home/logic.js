const course1 = () => {
  var classList = document.querySelectorAll(".input-value-1");
  const main = Array.prototype.map.call(classList, function (element) {
    return element.value;
  });
  let bbc = (n) => (isNaN(n) ? 0 : n);

  var sum = main.reduce(function (a, b) {
    return bbc(parseInt(a)) + bbc(parseInt(b));
  }, 0);

  if (sum >= 70) {
    return 5;
  }
  if (sum >= 60) {
    return 4;
  }
  if (sum >= 50) {
    return 3;
  }
  if (sum >= 45) {
    return 2;
  }
  if (sum < 45) {
    return 0;
  }
};

